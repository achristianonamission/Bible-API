function Genesis5(verse){
if(verse===1){
return "This is the book of the generations of Adam. In the day that God created man, in the likeness of God made he him;"
}
if(verse===2){
return "Male and female created he them; and blessed them, and called their name Adam, in the day when they were created."
}
if(verse===3){
return "And Adam lived an hundred and thirty years, and begat a son in his own likeness, and after his image; and called his name Seth:"
}
if(verse===4){
return "And the days of Adam after he had begotten Seth were eight hundred years: and he begat sons and daughters:"
}
if(verse===5){
return "And all the days that Adam lived were nine hundred and thirty years: and he died."
}
if(verse===6){
return "And Seth lived an hundred and five years, and begat Enos:"
}
if(verse===7){
return "And Seth lived after he begat Enos eight hundred and seven years, and begat sons and daughters:"
}
if(verse===8){
return "And all the days of Seth were nine hundred and twelve years: and he died."
}
if(verse===9){
return "And Enos lived ninety years, and begat Cainan:"
}
if(verse===10){
return "And Enos lived after he begat Cainan eight hundred and fifteen years, and begat sons and daughters:"
}
if(verse===11){
return "And all the days of Enos were nine hundred and five years: and he died."
}
if(verse===12){
return "And Cainan lived seventy years and begat Mahalaleel:"
}
if(verse===13){
return "And Cainan lived after he begat Mahalaleel eight hundred and forty years, and begat sons and daughters:"
}
if(verse===14){
return "And all the days of Cainan were nine hundred and ten years: and he died."
}
if(verse===15){
return "And Mahalaleel lived sixty and five years, and begat Jared:"
}
if(verse===16){
return "And Mahalaleel lived after he begat Jared eight hundred and thirty years, and begat sons and daughters:"
}
if(verse===17){
return "And all the days of Mahalaleel were eight hundred ninety and five years: and he died."
}
if(verse===18){
return "And Jared lived an hundred sixty and two years, and he begat Enoch:"
}
if(verse===19){
return "And Jared lived after he begat Enoch eight hundred years, and begat sons and daughters:"
}
if(verse===20){
return "And all the days of Jared were nine hundred sixty and two years: and he died."
}
if(verse===21){
return "And Enoch lived sixty and five years, and begat Methuselah:"
}
if(verse ===22){
return "And Enoch walked with God after he begat Methuselah three hundred years, and begat sons and daughters:"
}
if(verse===23){
return "And all the days of Enoch were three hundred sixty and five years:"
}
if(verse===24){
return "And Enoch walked with God: and he was not; for God took him."
}
if(verse===25){
return "And Methuselah lived an hundred eighty and seven years, and begat Lamech."
}
if(verse===26){
return "And Methuselah lived after he begat Lamech seven hundred eighty and two years, and begat sons and daughters:"
}
if(verse===27){
return "And all the days of Methuselah were nine hundred sixty and nine years: and he died."
}
if(verse===28){
return "And Lamech lived an hundred eighty and two years, and begat a son:"
}
if(verse===29){
return "And he called his name Noah, saying, This same shall comfort us concerning our work and toil of our hands, because of the ground which the Lord hath cursed."
}
if(verse===30){
return "And Lamech lived after he begat Noah five hundred ninety and five years, and begat sons and daughters:"
}
if(verse===31){
return "And all the days of Lamech were seven hundred seventy and seven years: and he died."
}
if(verse===32){
return "And Noah was five hundred years old: and Noah begat Shem, Ham, and Japheth."
}
}