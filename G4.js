function Genesis4(verse){
if(verse===1){
return "And Adam knew Eve his wife; and she conceived, and bare Cain, and said, I have gotten a man from the Lord."
}
if(verse===2){
return "And she again bare his brother Abel. And Abel was a keeper of sheep, but Cain was a tiller of the ground."
}
if(verse===3){
return "And in process of time it came to pass, that Cain brought of the fruit of the ground an offering unto the Lord."
}
if(verse===4){
return "And Abel, he also brought of the firstlings of his flock and of the fat thereof. And the Lord had respect unto Abel and to his offering:"
}
if(verse===5){
return "But unto Cain and to his offering he had not respect. And Cain was very wroth, and his countenance fell."
}
if(verse===6){
return "And the Lord said unto Cain, Why art thou wroth? and why is thy countenance fallen?"
}
if(verse===7){
return "If thou doest well, shalt thou not be accepted? and if thou doest not well, sin lieth at the door. And unto thee shall be his desire, and thou shalt rule over him."
}
if(verse===8){
return "And Cain talked with Abel his brother: and it came to pass, when they were in the field, that Cain rose up against Abel his brother, and slew him."
}
if(verse===9){
return "And the Lord said unto Cain, Where is Abel thy brother? And he said, I know not: Am I my brother's keeper?"
}
if(verse===10){
return "And he said, What hast thou done? the voice of thy brother's blood crieth unto me from the ground."
}
if(verse===11){
return "And now art thou cursed from the earth, which hath opened her mouth to receive thy brother's blood from thy hand;"
}
if(verse===12){
return "When thou tillest the ground, it shall not henceforth yield unto thee her strength; a fugitive and a vagabond shalt thou be in the earth."
}
if(verse===13){
return "And Cain said unto the Lord, My punishment is greater than I can bear."
}
if(verse===14){
return "Behold, thou hast driven me out this day from the face of the earth; and from thy face shall I be hid; and I shall be a fugitive and a vagabond in the earth; and it shall come to pass, that every one that findeth me shall slay me."
}
if(verse===15){
return "And the Lord said unto him, Therefore whosoever slayeth Cain, vengeance shall be taken on him sevenfold. And the Lord set a mark upon Cain, lest any finding him should kill him."
}
if(verse===16){
return "And Cain went out from the presence of the Lord, and dwelt in the land of Nod, on the east of Eden."
}
if(verse===17){
return "And Cain knew his wife; and she conceived, and bare Enoch: and he builded a city, and called the name of the city, after the name of his son, Enoch."
}
if(verse===18){
return "And unto Enoch was born Irad: and Irad begat Mehujael: and Mehujael begat Methusael: and Methusael begat Lamech."
}
if(verse===19){
return "And Lamech took unto him two wives: the name of the one was Adah, and the name of the other Zillah."
}
if(verse===20){
return "And Adah bare Jabal: he was the father of such as dwell in tents, and of such as have cattle."
}
if(verse===21){
return "And his brother's name was Jubal: he was the father of all such as handle the harp and organ."
}
if(verse===22){
return "And Zillah, she also bare Tubalcain, an instructer of every artificer in brass and iron: and the sister of Tubalcain was Naamah."
}
if(verse===23){
return "And Lamech said unto his wives, Adah and Zillah, Hear my voice; ye wives of Lamech, hearken unto my speech: for I have slain a man to my wounding, and a young man to my hurt."
}
if(verse===24){
return "If Cain shall be avenged sevenfold, truly Lamech seventy and sevenfold."
}
if(verse===25){
return "And Adam knew his wife again; and she bare a son, and called his name Seth: For God, said she, hath appointed me another seed instead of Abel, whom Cain slew."
}
if(verse===26){
return "And to Seth, to him also there was born a son; and he called his name Enos: then began men to call upon the name of the Lord."
}
}